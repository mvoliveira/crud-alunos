        Usuario aluno = dao.buscaUsuario(JLista.getSelectedValue());

        TxtMedia.setText(Float.toString(aluno.getMedia()));
        TxtNota1.setText(Float.toString(aluno.getNota1()));
        TxtNota2.setText(Float.toString(aluno.getNota2()));
