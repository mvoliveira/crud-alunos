    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
     UsuarioDAO dao = new UsuarioDAO();
     Usuario aluno = dao.buscaUsuario(JLista.getSelectedValue());
         dao.Remove(Integer.toString(aluno.getId()));
         TxtMedia.setText("");
         TxtNota1.setText("");
         TxtNota2.setText("");
         TxtNota3.setText("");
         TxtNota4.setText("");
         retornaLista();
        
        
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
      UsuarioDAO dao = new UsuarioDAO();
      Usuario aluno = dao.buscaUsuario(JLista.getSelectedValue());
      
      
      aluno.setMedia(Float.parseFloat(TxtMedia.getText()));
      aluno.setNota1(Float.parseFloat(TxtNota1.getText()));
      aluno.setNota2(Float.parseFloat(TxtNota2.getText()));
      aluno.setNota3(Float.parseFloat(TxtNota3.getText()));
      aluno.setNota4(Float.parseFloat(TxtNota4.getText()));
        
        dao.Alterar(aluno);

         
        
        
    }//GEN-LAST:event_jButton5ActionPerformed

